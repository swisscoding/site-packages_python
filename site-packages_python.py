from colored import stylize, fg
import site, platform

# decoration
print(stylize("\n---- | Installed site packages (Python) | ----\n", fg("red")))

# class
class SPPython:
    # output magic method
    def __repr__(self):
        version = self.py_version()
        path = stylize(self.get_site_packages(), fg("red"))
        return f"Path for Python {version}:\n{path}\n"

    # methods
    def py_version(self):
        return platform.python_version()

    def get_site_packages(self):
        return site.getsitepackages()[0]

# main execution
if __name__ == "__main__":
    print(SPPython())
